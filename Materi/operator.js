// Artimatika
let a = 10;
let b = 3;

console.log(a + b);  // 13
console.log(a - b);  // 7
console.log(a * b);  // 30
console.log(a / b);  // 3.333...
console.log(a % b);  // 1 (sisa pembagian)
console.log(a ** b); // 1000 (10 pangkat 3)

// Perbandingan
let x = 5;
let y = 10;

console.log(x == y);   // false (nilai berbeda)
console.log(x != y);   // true (nilai berbeda)
console.log(x < y);    // true (x lebih kecil dari y)
console.log(x === 5);  // true (nilai dan tipe sama)
console.log(x === "5"); // false (nilai sama, tapi tipe berbeda)

// Logika
let c = 5;
let d = 10;

console.log(c > 0 && d > 0);  // true (keduanya lebih besar dari 0)
console.log(c > 0 || d < 0);  // true (salah satu kondisi benar)
console.log(!(c < 0));        // true (negasi dari false = true)
